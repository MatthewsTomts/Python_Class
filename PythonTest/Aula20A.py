def lin():  # Uma função, facilmente discenível pelo início 'def'
    print('-'*30)


def msg(mensagem):  # Cria uma mensagem personalizada
    lin()  # Chama a função 'lin'
    print(f'{mensagem:^30}')
    lin()


msg('Curso em Vídeo')  # Chama a função 'msg'

msg('Aprenda Python')

msg('Matheus Farias')

